/**
 * Generate transfer content from email and plan name
 * Format: username_planname (without accents and spaces)
 * Example: user@example.com + "VIP Tháng" -> "user_vipthang"
 */
export function generateTransferContent(email: string, planName: string): string {
  // Get username part (before @)
  const username = email.split('@')[0];
  
  // Remove accents from plan name and convert to lowercase
  const planNameNoAccents = removeVietnameseAccents(planName)
    .toLowerCase()
    .replace(/\s+/g, ''); // Remove all spaces
  
  // Combine with underscore
  return `${username}_${planNameNoAccents}`;
}

/**
 * Validate transfer content format
 */
export function isValidTransferContent(content: string): boolean {
  // Should contain only alphanumeric characters and underscore
  return /^[a-zA-Z0-9_]+$/.test(content);
}