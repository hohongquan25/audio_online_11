/**
 * Generate a random 9-character alphanumeric code
 * Format: 9 random characters (uppercase letters and numbers)
 * Example: "A3K9M2P7Q"
 */
export function generateUserCode(): string {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  let code = '';
  
  for (let i = 0; i < 9; i++) {
    const randomIndex = Math.floor(Math.random() * chars.length);
    code += chars[randomIndex];
  }
  
  return code;
}

/**
 * Validate user code format
 */
export function isValidUserCode(code: string): boolean {
  // Should be exactly 9 alphanumeric characters
  return /^[A-Z0-9]{9}$/.test(code);
}

/**
 * Generate transfer content from user code and plan name
 * Format: code_planname (without accents and spaces)
 * Example: "K7M3P9A2X" + "VIP Tháng" -> "K7M3P9A2X_vipthang"
 */
export function generateTransferContent(userCode: string, planName: string): string {
  // Remove accents from plan name and convert to lowercase
  const planNameNoAccents = removeVietnameseAccents(planName)
    .toLowerCase()
    .replace(/\s+/g, ''); // Remove all spaces
  
  // Combine with underscore
  return `${userCode}_${planNameNoAccents}`;
}
